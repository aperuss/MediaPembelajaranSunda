            <p class="flex justify-center text-xl md:text-2xl font-extrabold mb-4">Mikawanoh Drama</p>
            <div class="leading-7 indent-6 md:indent-12 text-justify">
                Drama téh nya éta carita atawa lalakon anu diwujudkeun dina wangun paguneman, maksudna pikeun dipintonkeun. Anu kaasup garapan sastra dina drama nyaéta naskahna anu jadi dadasar pintonan drama.
              </div>
            <div class="leading-7 indent-6 md:indent-12 text-justify">
                Karangan drama téh boga rupa-rupa ciri. Upama ditilik tina kalimah anu digunakeunana, drama gegedéna ngagunakeun kalimah langsung, a éta kalimah anu dikedalkeun ku palaku. Dina drama anu 
                palakuna пуа leuwih ti saurang, gelarna paguneman téh mangrupa omongan palaku anu silitémpas (dialog). Tapi aya éta ogé bagéan palaku anu ngomong sorangan (monolog).
            </div>
            <div class="leading-7 indent-6 md:indent-12 text-justify">
                Salian ti éta, dina karangan drama aya bagéan anu nerangkeun tingkah laku palaku, latar tempat, jeung latar waktu, nya éta katerangan anu kudu dicangkem boh ku nu nyekel peran boh ku sutradara, upama éta naskah drama téh rék dipintonkeun. Kaasup saha anu nyarita dina paguneman. Éta katerangan téh disebutna pituduh (stage direction).
            </div>
