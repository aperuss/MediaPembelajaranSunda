                B. Midangkeun iklan Layanan Masarakat dina Poster<br>
                Hidep resep ngagambar, ngalukis, atawa ngadesain? Poster misalna. Tah geura, poster ogé jadi media nu alus pikeun midangkeun hiji hal. Misalna midangkeun iklan layanan masarakat. Sok komo, kiwari mah loba pisan kagiatan-kagiatan anu sipatna pasanggiri ngadesain poster bari hadiahna kawilang badag   
            </p>
            <p class="mb-4">
                Iklan layanan masarakat mimiti diwanohkeun di Amérika Serikat dina taun 1942 nalika diwangun The Advertising Council anu satuluyna disebut jadi Dewan Iklan. Iklan layanan masarakat nyaéta iklan anu midangkeun pesen-pesen sosial nu tujuanana 
                pikeun ngahudang karep masarakat kana sajumlahing masalah anu sumebar dina kahirupan manusa tur kondisina bisa méré pangaruh negatif pikeun kahirupan umum. 
                Iklan layanan masarakat biasana ditepikeun ku instansi pamaréntah, LSM atawa yayasan sosial. Aya sababaraha pasaratan anu kudu diperhatikeun nalika nyieun iklan layanan masarakat.
                Iklan layanan masyarakat mangrupa salah sahiji stratégi anu bisa digunakan ku pamaréntah pikeun sosialisasi program pangwangunan. Di tingkat nasional, pamaréntah leuwih milih télévisi pikeun 
                media midangkeun iklan. Cara séjén nu dianggap leuwih murah pikeun midangkeun iklan layanan masarakat ka balarea di antarana bisa ngaliwatan baliho, spanduk, atawa poster.
                Kumaha prak-prakan midangkeun iklan dina poster?
                Sabada hidep apal kumaha konsep iklan layanan masarakat dina pedaran di luhur ayeuna urang pedar perkara poster. Poster nyaéta wawaran atawa iklan dina wangun gambar atawa tulisan nu eusina uar pangajak ngalakukeun hal-hal anu positif. 
                Ciri-ciri poster (1) basajan, (2) midangkeun ide tur miboga tujuan, (3) colorfull, (4) midangkeun slogan, (5) tulisan kabaca tur jéntré, Umumna poster dipajang di mading atawa tempat-tempat tertentu nu babari diakses ku masarakat. Ilikan conto poster ieu di handap!                
