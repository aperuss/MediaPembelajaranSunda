            Mikawanoh Artikel Budaya Sunda<br>
            Dina ungkara "artikel budaya Sunda" aya dua perkara ano kuis dipedar téh, nya éta perkara artikel jeung perkara budaya Sunda.<br>
            Artikel sok disebut ogé pedaran nya éta karangan basa lancaran anu eusina ngaguar, medar, ngabahas, atawa ngadadarkeun hiji perkara anu ngagunakeun katerangan-katerangan nepi ka bubuk leutikna. Anu dipuler sangkan anu maca meunang katerangan (informasi) anu tétéla ngeunaan hiji perkara.<br>          
            Arti kecap "budaya" asalna tina basa Sangsekerta anu ngandung harti "pikiran, akal, pangaweruh". Kiwari ngandung harti sagala rupa hasil pikiran, akal, jeung pangaweruh manusa. Budaya Sunda nya éta hasil budaya sélér bangsa (urang) Sunda.<br>           
            Sacara jembar, budaya ngawengku unsur-unsur:<br>
            1. Kasenian.<br>
            2. Téhnologi jeung pakakas.<br>
            3. Tata cara hirup kumbuh di masarakat.<br>
            4. Basa.<br>  
            5. Pacarian jeung ekonomi.<br>
            6. Élmu pangaweruh.<br>            
            7. Kapercayaan (agama).<br>
            Upama nili ka dinya, anu kaasup kana budaya atawa kabudayaan Sunda téh lain ngan saukur basa, kasenian atawa adat caharana wungkul tapi ogé ngurung sagala rupa hal anu aya patalina jeung kahirupan sosial masarakat Sunda.<br>
            Unggal bangsa atawa sélér bangsa miboga budaya anu mandiri, anu béda jeung bangsa atawa sélér bangsa séjénna. Sanajan aya dina amparan pulo anu sarua, upamana, sélér bangsa Sunda miboga budaya anu béda jeung sélér bangsa Jawa. Tong jauh-jauh, anu sapropinsi baé ogé, urang sunda miboga budaya anu béda jeung urang Cirebon. Kitu deui jeung urang Batawi anu aya di wewengkon Bekasi, Dépok, jeung Bogor.<br>
            Mémang unsur budaya anu panggampangna kaciri bédana téh nya ata basa, kasenian, jeung adat istiadatna. Anu matak henteu anéh upama anu medar budaya hiji sélér bangsa, umumna henteu jauh tina perkara basa, kasenian, jeung adat istiadat kaasup upacara adatna.<br>
            Bedana budaya unggal sélér bangsa téh, boh pikeun Propinsi Jawa Barat boh pikeun Nagara Indonésia, mangrupa ciri beungharna budaya urang. Kabeungharan budaya anu sawadina dijaga, diriksa, jeung dimumulé.<br>             
