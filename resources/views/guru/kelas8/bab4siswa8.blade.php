        </p>
        <p class="mb-4">
            Dina bacaan di luhur, aya kalimah-kalimah anu unina kieu:<br>

            1. Goong rénténg téh kasenian ti Kabupatén Bandung.<br>
            
            2. Acara dipungkas ku ngabagikeun kadaharan anu meunang ngumpulkeun téa.<br>
            
            3. Dina penca aya pola-pola gerak anu ngamangpaatkeun tanaga jeung kakuatan.<br>
            
            Kecap-kecap anu dicondongkeun dina kalimah-kalimah di luhur téh mangrupa kecap rundayan anu maké rarangkén barung ka--an. Nya éta kasenian, kadaharan, jeung kakuatan.<br>
            
            Upama dirucat prosésna mah kieu:<br>
            
            seni + ka-an -> kasenian<br>
            
            dahar + ka-an -> kadaharan<br>
            
            kuat + ka-an -> kakuatan<br>
            
            Rarangkén barung ka-an dina basa Sunda miboga fungsi, di antarana:<br>
            
            1. Ngawangun kecap barang:<br>
            
            -	 ngandung harti "anu patali jeung...", contona: kakuatan, kaéndahan, kageulisan, kabudayaan.<br>
            
            -	anu ngandung harti "anu di ...", contona: kadaharan, kaolahan. anu ngandung "anu di-....keun", contona: kapentingan,kaperluan.<br>
            
            -	anu ngandung harti "anu sipatna....", contona: kanyataan, kajayaan, kabiasaan.<br>
            
            -	anu ngandung harti "tempat anu patali jeung ...", contona: kacamatan, kadaleman, kawadanaan.<br>
            
            2. Ngawangun kecap pagawéan (pasif):<br>
            
            -	anu ngandung harti "teu dihaja kakeunaan ku ...", contona: kadiukan, kacaahan, kakotoran.<br>
            -	Anu ngandung harti "teu dihaja kakeunaan ku pagawéan...", contona: kabangsatan, kacopétan.<br>
            -	Anu ngandung harti "ten dihaja aya dina kaayaan ...” contona kalaparan, kakurangan.<br>
            -	anu ngandung harti "bisa di- ... - an", contona: kacumponan, katedunan.<br>
            
            3. Ngawangun kecap sipat<br>
            
            -	anu ngandung harti "bisa di-...-keun", contona: kabuktian,kapastian.<br>
            
            -	anu ngandung harti "teu dihaja...”   contona: kabeneran,kalepasan.<br>
            
            -	anu ngandung harti "teu dihaja kakeunaan ...”  contona: kabeurangan, kapanasan, katiisan.<br>            
        </p>

