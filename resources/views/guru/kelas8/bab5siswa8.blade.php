            <p class="mb-4">
                1. Struktur Téks Gelar Wicara (Talk Show)<br>
                Jadi protokol dina gelar wicara atawa talk show merlukeun kaparigelan palanggeran-palanggeran anu geus disusun dina naskah gelar wicara. Struktur naskah gelar wicara atawa talk show kabagi jadi tilu bagian:<br>
                a. Bagian bubuka<br>
                Biasana dimimitian ku protokol muka acara, ngawanohkeun dirina, ngaWanohkeun narasumber jeung nyebutkeun jejer acara.<br>
                Contona:
                Pamiarsa tepang deui dina pidangan "Nyiar Luang tina Daluang tangaran pasundan di Bandung TV, Kaleresan dina dinten ieu parantos sumping di sagedengeun sim kuring Bapa DR. yayat Hendayana, S.S., M.hum. Urang 
                Bandung pituin, lahir taun 1943, anu badé ngaguar perkawis "kabudayaan daérah." Kungsi janten aktor téater, ogé kantos kapilih aktor panghadéna dina acara "Téater Empat Kota". Bapa Yayat ogé aktip dina sababaraha 
                organisasi, di antarana waé kungsi jadi Ketua Persatuan Wartawan Indonésia atawa PWI Jawa Barat taun 1983 dugi ta taun 1988, ogé kungsi janten Ketua Lembaga Basa Jeung Sastra Sunda Atawa LBSS. anggota Dewan Kebudayaan Jawa Barat, sareng sajabina. Sampurasun Pa Yayat.<br> 
                b. Bagian inti<br>
                Dina bagian inti, protokol méré kasempetan ka narasumber pikeun medar hiji hal, ngajawab, nepikeun pamadegan ngeunaan jejer nu keur diguar. Bagian eusi biasana bakal dijejeran ku protokol luyu jeung waktu nu disayagikeun.<br>
                Contona:
                Ayeuna dina patarosan anu munggaran ieu téh, Pa, nya. leu aya patarosan perkawis kabudayaan daérah.<br>
                c. Bagian panutup<br>
                Protokol boga pancén ngatur waktu lumangsungna talk show. Dina bagian ahir acara biasana pemandu bakal nutup acara ku kalimah saperti ieu di handap.<br>
                Hatur nuhun ka pangersa Bapa Yayat parantos sumping, parantos ngawaler patarosan-patarosan pamiarsa nu aya di bumi ogé, mugi urang tiasa tepang deui, tiasa ngobrol langkung euyeub deui perkawis kabudayaan, basa, sareng sajabina. Ngawakilan ais pangampih amit mundur. Wassalamualaikum Wr. Wb. Tangara Pasundan sim kuring pun Astri Cahyani, seja amit mungkur
                Saliwat, boh bagian bubukana boh bagian panutupna méh sarua jeung muka atawa nutup midangkeun warta. Bakal gampang hidep nuturkeunana lantaran hidep boga dasar pangaweruh jeung pangalaman tina pangajaran saméméhna. Bral ulik deui masing saregep.
