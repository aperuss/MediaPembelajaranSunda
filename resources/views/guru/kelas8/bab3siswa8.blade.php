            D. Prakték Nulis Warta<br>
            Nulis warta téh henteu bangga. Hidep bisa ngamimitian ngumpulkeun data 
            jeung fakta sabada manggihan topik nu bakal diwartakeun. Kagiatan- kagiatan di 
            sakola hidep loba anu bisa dipaké bahan nulis warta. Misalna; kagiatan 
            muludan, panén karya P5, péntas seni, pemilihan ketua OSIS, jeung sajabana. 
            Ayeuna pék hidep babagi pancén jeung batur sakelompok hidep. Jieun hiji téks 
            warta. Tuturkeun léngkah-léngkah anu geus disadiakeun ieu di handap!
        </p>
