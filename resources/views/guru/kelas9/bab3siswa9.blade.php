              <p class="font-extrabold text-xl">1. Paribasa</p>
              <div class="leading-7 indent-6 md:indent-12 text-justify mb-2">
                Dina pedaran ti heula geus disebutkeun, yén 
                paribasa téh mangrupa babandingan kalakuan atawa kaayaan jelema kana kalakuan 
                atawa kaayaan hiji hal, anu bisa jadi perlambang lakuning hirup. Minangka pakéman 
                basa, paribasa ogé kekecapanana geus matok. Paribasa "monyét ngagugulung 
                kalapa", upamana, henteu bisa diganti jadi "jelema ngagugulung kalapa" atawa 
                "monyét ngagugulung samangka".
              </div>
              <div class="leading-7 indent-6 md:indent-12 text-justify mb-2">
                Geus puguh éntép-seureuhna jeung pok
                pokanana deuih. Henteu bisa dirobah jadi "kalapa digugulung ku monyét" atawa 
                "monyét ngagulung-gulung kalapa". Sakumaha umumna pakeman basa, paribasa 
                ogé henteu bisa dirobah jadi ragam basa hormat (dilemeskeun). Dina wangunna, 
                paribasa mangrupa kalimah. Béda jeung babasan, anu mangrupa gabungan kecap, 
                frasa atawa kecap kantétan 
