                1. Kabeungharan Kecap<br> 
                Dilakonan Sangkan maham rumpaka kawih di luhur hayu urang pedar heula geuro harti 
                kecap anu sakirana hésé. Pék angkem éta kecap-kecap anu dipedar téh.<br>
                Dilakonan : Asal kecapna lakon, biasana sok dipasangkeun jeung kecap gawé, lakon gawé nu 
                hartina ngajalankeun pagawéan. Dilakonan (di-an + lakon), hartina dipaju, disanghareupan, 
                dijalankeun, nyorang kahirupan.<br>
                Datar: Rata. Kecap datar di dieu mangrupa harti injeuman, maksudna sanada, lempeng, 
                angger. Kahirupan téh teu angger.<br>
                Nepak dada : harti injeuman, maksudna sombong, adigung<br>
                Éling : inget, jejeg pikiran<br>
                Kufur : (Bs. Arab) ingkar tina kaimananana<br> 
                Jalma gedé : harti injeuman, maksudna jalma anu hirupna sukses<br>
                Jembar : lega<br>
                Ngajalma : harti injeuman, maksudna jadi jalma anu bener, pinter, cageur<br>
                Dalit: ngahiji<br>
                Owah gingsir: (babasan) hartina robah sifat<br>
                Ngageuing : sipat Asal kecapna geuing. Ngageuing hartina ngingetan jelema supaya ulah 
                ngalakukeun lampah goréng.<br>
                Raksa : miara, nalingakeun<br>
                Tabéat: (bs. Arab) hartina adat kabiasaan<br>
