                Dumasar kana cara midangkeun tulisanana, wangun téks rumpaka kawih ditulis dina 
                wangun ugeran (puisi). Di antarana kauger ku ayana padalisan, pada, jeung purwakanti 
                sora. Hal-hal kasebut nembrak kabaca tur babari dituduhkeun atawa ditéangan ku cara 
                maca kalayan gemet rumpaka kawihna. Bisa disebutkeun waruga lahir nu babari 
                katempo ku mata lahir. Sedeng unsur-unsur téks rumpaka kawih mah bisa disebutkeun 
                waruga batin, kaayaan batin nu aya di jero ungkara kalimah-kalimahna. Ku lantaran ayana 
                dina jero rumpaka kawihna henteu babari ditéangan, henteu babari kapanggih saupama 
                dibaca. Cara manggihanana tinangtu merlukeun kaparigelan urang dina nyurahan atawa 
                ngalenyepan eusi téks rumpaka kawih. Dibaca deui sababaraha kali, dilenyepan sarta 
                disurahan atawa diinterprétasi naon eusi maksudna. Ayeuna urang analisis unsur-unsur 
                intrinsik dina rumpaka kawih "Manuk Dadali”.
