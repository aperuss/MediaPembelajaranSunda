            <p class="mb-4">
                <b>a. Téma </b><br>
                Téma dina karya sastra mah mémang henteu nembrak. Teu babari ditéangan. Bakal 
                muncul atawa kapanggih sabada urang réngsé maca éta karya sastra. Téma téh 
                nyaéta inti pikiran atawa puseur implengan pangarang. Misalna, téma kawih "Manuk 
                Dadali" nyaéta manuk nu jadi perlambang karakter nu luhung, karakter sinatria 
                salaku warga nagara nu mikacinta bangsa jeung nagarana.<br>
                <b>b. Rasa</b><br> 
                Rasa di dieu nyoko kana rasa pangarang anu digambarkeun dina karyana. Biasana 
                nepi ogé ka diri nu maca sabada tamat maca hiji karya sastra. Rasa raket patalina 
                jeung unsur émosi nu maca. Sedih, ambek, bungah, sumanget jeung sajabana. 
                Misalna, rasa anu muncul sabada urang maca atawa ngupingkeun kawih "Manuk 
                Dadali" di antarana nyaéta ngahudang rasa sumanget.<br>
                <b>c. Nada</b><br>
                Nada raket patalina jeung gaya pangarang nuliskeun karyana. Unggal pangarang 
                miboga ciri has dina ngagunakeun basana, kalimah- kalimahna, pilihan kecapna, 
                purwakantina (padeukeutna sora) jeung sajabana. Misalna, dina rumpaka kawih 
                "Manuk Dadali" pangarang ngagunakeun kecap-kecap nu jadi perlambang jiwa 
                pejuang, jiwa satria, wawanén jeung sauyunan hirup dina mikacinta bangsa jeung 
                nagara.<br> 
                <b>d. Amanat</b><br> 
                Amanat téh nya éta pesen atawa talatah ti pangarang nu ditepikeun ngaliwatan 
                karyana pikeun nu maca atawa nu ngaregepkeun. Misalna, dina rumpaka kawih 
                "Manuk Dadali" leubeut pisan ku pesen atawa amanat nu hayang ditepikeun ku 
                pangarang ka nu maca atawa anu ngupingkeun kawihna, di antarana: Kudu rukun 
                sauyunan dina hirup kumbuh sapopoé Kudu lébér wawanén dina ngabéla bangsa 
                jeung nagara.
