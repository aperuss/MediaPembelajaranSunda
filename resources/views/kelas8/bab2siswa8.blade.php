        <p class="mb-4">
          B. Pedaran Carita Pondok<br>
          Carita pondok sok disingget jadi carpon. Carita pondok nyaéta carita rékaan (fiksi) tina 
          laku manusa dina kahirupan sapopoé. Eusina biasana teu leupas tina ajén atikan jeung 
          moral pikeun pieunteungeun atawa picontoeun hususna pikeun anu macana. Carpon 
          kaasup karya sastra dina wangun lancaran (prosa). Ku lantaran pondok téa, kajadian anu 
          dicaritakeunana ogé henteu réa, biasana ngan diwangun ku hiji kajadian utama. Boh 
          tokohna, latarna, waktuna, biasana mah teu pati loba.
        </p>  
        <p class="mb-4">
          1.	Unsur-Unsur Intrinsik dina Carita Pondok <br> 
          Geus disebutkeun di luhur yén carpon téh kaasup kana karya sastra wangun prosa 
          (lancaran). Unsur-unsur intrinsik dina carita pondok anu rék dipedar di dieu nyaéta:<br>
          A. Téma<br>
          Urang kudu maca éta carita sagemblengna. Téma dina carita "Jimat" misalna, bisa 
          saperti kieu: kakuatan hiji barang atawa Téma nyaéta inti pikiran nu aya dina prosa 
          fiksi. Pikeun nangtuke mah teu bisa diandelkeun sabab aya kakuatan nu leuwih 
          agung sagala barang jeung jimat nyaéta kakuatan Allah SWT.<br>
          B. Tokoh<br> 
          Tokoh atawa palaku nyaéta anu ngalalakon dina hiji carita, boh atawa sasatoan. 
          Dina hiji carita tokoh atawa palaku téh aya anud palaku utama, palaku kadua, 
          jeung palaku tambahan. Contona carpon "Jimat":<br> 
          palaku utama: Ujang, Mang tawi<br> 
          palaku kadua: Ékéng, Ihok, Sahri<br>
          palaku tambahan: Kang Ita, Haji Ismail, Bah Alpasan, Madtasan, Konon, Uman, Sanroip, Si Ruyung.<br>
          C. Latar<br>
          Latar téh aya dua rupa, latar tempat jeung latar waktu. Latar tem mah biasana 
          nuduhkeun di mana kajadianana ari latar waktu s nuduhkeun iraha kajadianana. 
          Contona dina carpon "Jimat".<br>
          Latar tempat: émpér warung, sisi pasar, Cihaurgeulis, Jatibunde.<br> 
          Latar waktu: wanci haneut moyan, wayah asar.<br> 
          D. Galur<br> 
          Galur nyaéta runtuyan kajadian anu ngawangun leunjeuran carita.<br>
          E. Amanat<br>
          Amanat nyaéta pesen pangarang nu hayang ditepikeun ka nu mo ngaliwatan 
          karyana. Amanat dina carpon "Jimat" upamana baé kie ulah percaya kana hiji 
          barang atawa jimat nu dianggap boga kakuata méjik da percaya mah sasatna 
          kudu ka Gusti.<br> 
