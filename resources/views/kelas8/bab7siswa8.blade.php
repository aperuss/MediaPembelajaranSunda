                <p class="font-extrabold text-2xl">B. Mikawanoh Sisindiran</p>
                <div class="leading-7 indent-6 md:indent-12 text-justify">
                    Sisindiran asal kecapna sindir. Hartina ngomong malibir, henteu sajelasna. Nyindiran hartina ngomong ku sindir. Sindir-sampir hartina ngomong ku sindir sababaraha kali. Sisindiran kaasup kana papaés basa nu ngahaja disusun dialus-alus. Sisindiran tujuanana pikeun nyindiran. Carana, ngagunakeun sindir téa bari boga maksud supaya henteu nerag teuing turun
                    teu mantak nyigeung rasa nu disindirna.
                </div>
                <div class="leading-7 indent-6 md:indent-12 text-justify">
                    Ari dina sisindiran, umumna eusi atawa maksud anu dikedalkeun téh sok dibungkus ku cangkang. Ku lantaran kitu, wangunan sisindiran téh kauger ku hal-hal saperti kieu; (1) purwakanti, (2) jumlah engang dina unggal padalisan, (3) jumlah padalisan dina unggal pada, (4) aya cangkang jeung eusi. Imeutan conto sisindiran ieu di handap:
                </div>
