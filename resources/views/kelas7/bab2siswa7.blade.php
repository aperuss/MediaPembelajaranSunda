      <p class="text-gray-700 mb-4">
        Kampung Adat Cireundeu nyaéta hiji kampung anu ayana di lamping Gunung Kunci,Gunung Ciménténg jeung Gunung Gajahlangu. Sacara administratif, Kampung Adat 
        Cireundeu pernahna di Kelurahan Leuwigajah, Kecamatan Cimahi Selatan, Kota Cimahi. Éta wewengkon dicicingan ku 50 kulawarga, jumlah pendudukna kurang leuwih 800 jiwa. 
        Sabagian gedé pakasaban masarakatna kana tatanén, nyaéta ngarebon sampeu. Kampung Adat Cireundeu legana 64 héktar, 60 hektar diantarana mangrupa lahan pertanian sarta nu 4
        héktar deui minangka lahan pamatuhan. 30 Wanda Basa Sunda pikeun Murid SMP/MTs Kelas VII Istilah Cireundeu diwangun ku dua kecap, nyaéta ci (cai) jeung reundeu. Reundeu nya 
        éta ngaran hiji daun, tangkalna di leuweung sarta bisa dilalab, tur rupana jiga daun sampeu. Ceuk kolot baheula mah daun reundeu téh sok digunakeun pikeun ubar panyakit leungeun. 
        Kiwari éta daun téh geus hésé dipanggihanana. Secara fisik, Cireundeu teu béda jeung umumna hiji kampung. Misalna dina segi wangunan imah pendudukna. Di antarana maké hateup tina kenténg, 
        batana ditémbok. jandélana tina kaca, jeung méh sakabéh pendudukna geus miboga alat éléktronik saperti télévisi, radio, kaasup telepon sélulér atanapi hp téa. Tapi ku lantaran kukuh dina 
        ngagem tradisi karuhun, ieu kampung dijadikeun sacara de facto jadi kampung adat. 
      </p>
        <p class="text-gray-700 mb-4">
        Masarakat Kampung Adat Cireundeu masih kénéh pengkuh nyekel ajaran tradisi karuhunna. Kapercayaan anu diagemna 
        nyaéta kapercayaan Sunda wiwitan. nu dibawa ku Pangéran Madrais ti Cigugur, Kuningan. Masarakat Kampung Cireundeu di antarana masih kénéh ngalaksanakeun Upacara Adat Tutup Taun Saka anu dilaksanakeun saban taun. 
        leu upacara dilaksanakeun dina raraga mapag taun anyar Kalender Sunda. Waktu tutup Taun Saka biasana méh bareng jeung Taun Anyar Islam. Tangal 1 Sura pikeun warga Cireundeu, ampir sarua jeung lebaran. Dina upacara adat, 
        kaum lalaki maké baju pangsi warna hideung, ari kaum awéwé mah maké kabaya atawa baju warna bodas. Gunungan sasajén. mangrupa bungbuahan jeung rasi, anu dibawa ka tengah ririungan (kumpulan) warga di Balé Adat. 
        </p>
        <p class="text-gray-700 mb-4">
